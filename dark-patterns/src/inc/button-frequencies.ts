function getScore(
  dictionary: { [name: string]: number },
  wordlist: string[]
): number {
  let returnVal = 0;
  let foundWord = false;
  for (let i = 0; i < wordlist.length; i++) {
    if (wordlist[i] in dictionary) {
      foundWord = true;
      returnVal = returnVal + dictionary[wordlist[i]];
    }
  }

  returnVal = returnVal / wordlist.length;

  if (foundWord) {
    return returnVal;
  } else {
    return 0;
  }
}

function getAcceptButtonScore(wordlist: string[]): number {
  return getScore(cookiePopupTextData.accept, wordlist);
}

function getRejectButtonScore(wordlist: string[]): number {
  return getScore(cookiePopupTextData.reject, wordlist);
}

function getManageButtonScore(wordlist: string[]): number {
  return getScore(cookiePopupTextData.manage, wordlist);
}

